type Locale = { ru: string; he: string; el: string };
type TranslationMap = Record<string, Locale>;

// Helper: creates a flat patch object from nested translations
// e.g. { "heroTitle.ru": "...", "heroTitle.he": "...", ... }
function buildPatch(translations: TranslationMap): Record<string, string> {
  const patch: Record<string, string> = {};
  for (const [field, t] of Object.entries(translations)) {
    patch[`${field}.ru`] = t.ru;
    patch[`${field}.he`] = t.he;
    patch[`${field}.el`] = t.el;
  }
  return patch;
}

