async function patchDocument(docType: string, translations: TranslationMap) {
  const docId = docType; // singletons use _type as _id
  const patch = buildPatch(translations);

  // Try published first, then draft
  for (const id of [docId, `drafts.${docId}`]) {
    try {
      await client.patch(id).set(patch).commit();
      console.log(`  ✓ ${docType} (${id}) — ${Object.keys(translations).length} fields patched`);
      return;
    } catch {
      // Try next id
    }
  }
  console.log(`  ✗ ${docType} — document not found`);
}

